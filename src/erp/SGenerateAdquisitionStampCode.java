    private void initComponentsExtra() {
        moSimpleDateFormat = new  SimpleDateFormat("dd/MM/yyyy");
        moSimpleDateFormat.setTimeZone(TimeZone.getTimeZone("GMT-06:00"));
        TimeZone.setDefault(TimeZone.getTimeZone("GMT-06:00"));
        
        try {
            jftDate.setFormatterFactory(new DefaultFormatterFactory(new MaskFormatter(SGuiUtils.createMaskFormatterDatetime(moSimpleDateFormat.toPattern()))));
        }
        catch (ParseException e) {
            System.out.println(e);
        }
        
        jcbPac.removeAllItems();
        jcbPac.addItem(new SGuiItem(new int[] { 1 }, "FORCOGSA"));
        jcbPac.addItem(new SGuiItem(new int[] { 2 }, "FINKOK"));
        
        jftDate.setValue(moSimpleDateFormat.format((Date) new Date()));
        jtfQuantity.setText("");
        jtfCode.setText("");
        jtfCode.setEditable(false);
    }

    private void actionProcess() {
        try {
            jtfCode.setText(STrnUtilities.getCodeActivationStamp(((SGuiItem) jcbPac.getSelectedItem()).getPrimaryKey()[0], moSimpleDateFormat.parse(jftDate.getText()), Integer.parseInt(jtfQuantity.getText())));
        }
        catch (ParseException | NumberFormatException e) {
            System.out.println(e);
        }
    }

    private void actionClose() {
        dispose();
        System.exit(0);
    }

