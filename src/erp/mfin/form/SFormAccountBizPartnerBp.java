    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moAccountBizPartnerBp = (SDataAccountBizPartnerBp) registry;

        moFieldPkBookkeepingCenterId.setFieldValue(new int[] { moAccountBizPartnerBp.getPkBookkepingCenterId() });
        moFieldPkBizPartnerCategoryId.setFieldValue(new int[] { moAccountBizPartnerBp.getPkBizPartnerCategoryId() });
        moFieldPkBizPartnerId.setFieldValue(new int[] { moAccountBizPartnerBp.getPkBizPartnerId(), moAccountBizPartnerBp.getPkBizPartnerCategoryId() });
        moFieldPkDateStartId.setFieldValue(moAccountBizPartnerBp.getPkDateStartId());
        moFieldIsDeleted.setFieldValue(moAccountBizPartnerBp.getIsDeleted());

        if (!moAccountBizPartnerBp.getIsRegistryNew()) {
            jcbPkBookkeepingCenterId.setEnabled(false);
            jbPkBookkeepingCenterId.setEnabled(false);
            jcbPkBizPartnerCategoryId.setEnabled(false);
            jbPkBizPartnerCategoryId.setEnabled(false);
            jcbPkBizPartnerId.setEnabled(false);
            jbPkBizPartnerId.setEnabled(false);
            jftPkDateStartId.setEnabled(false);
            jbPkDateStartId.setEnabled(false);
        }

        jckIsDeleted.setEnabled(!moAccountBizPartnerBp.getIsRegistryNew());
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moAccountBizPartnerBp.getIsRegistryNew()) {
            moAccountBizPartnerBp.setPkBookkepingCenterId(moFieldPkBookkeepingCenterId.getKeyAsIntArray()[0]);
            moAccountBizPartnerBp.setPkBizPartnerCategoryId(moFieldPkBizPartnerCategoryId.getKeyAsIntArray()[0]);
            moAccountBizPartnerBp.setPkBizPartnerId(moFieldPkBizPartnerId.getKeyAsIntArray()[0]);
            moAccountBizPartnerBp.setPkDateStartId(moFieldPkDateStartId.getDate());
            moAccountBizPartnerBp.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moAccountBizPartnerBp.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moAccountBizPartnerBp.setIsDeleted(moFieldIsDeleted.getBoolean());

        moAccountBizPartnerBp.setDbmsBookkeepingCenter(((SFormComponentItem) jcbPkBookkeepingCenterId.getSelectedItem()).getItem());
        moAccountBizPartnerBp.setDbmsBizPartnerCategory(((SFormComponentItem) jcbPkBizPartnerCategoryId.getSelectedItem()).getItem());
        moAccountBizPartnerBp.setDbmsBizPartner(((SFormComponentItem) jcbPkBizPartnerId.getSelectedItem()).getItem());

        return moAccountBizPartnerBp;
    }

