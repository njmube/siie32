    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(mvFields.get(i).getComponent());
                break;
            }
        }

        if (!validation.getIsError()) {
            if (moAccountBizPartnerBp.getIsRegistryNew()) {
                // Validate account:

                if (SDataUtilities.callProcedureVal(miClient,
                        SProcConstants.FIN_ACC_BP_BP_VAL, new Object[] {
                        moFieldPkBizPartnerId.getKeyAsIntArray()[0],            // business partner ID
                        moFieldPkBizPartnerId.getKeyAsIntArray()[1],            // category ID
                        moFieldPkBookkeepingCenterId.getKeyAsIntArray()[0],
                        moAccountBizPartnerBp.getPkAccountBizPartnerId(),
                        moFieldPkDateStartId.getDate()}, SLibConstants.EXEC_MODE_VERBOSE) > 0) {
                    validation.setMessage("Ya existe un registro para la combinación de parámetros especificados.");
                    validation.setComponent(jcbPkBookkeepingCenterId);
                }
            }
        }

        return validation;
    }

