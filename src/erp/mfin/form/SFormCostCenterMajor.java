    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(mvFields.get(i).getComponent());
                break;
            }
        }

        if (moCostCenter == null && !validation.getIsError()) {
            if (moFieldAccountPkCostCenterId.isEmptyAccountId()) {
                validation.setMessage(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlPkCostCenterId.getText() + "'.");
                validation.setComponent(jftPkCostCenterId);
            }
            else if (SDataUtilities.callProcedureVal(miClient, SProcConstants.FIN_CC_VAL,
                    new Object[] { SDataUtilities.composeCostCenterId(miClient, moFieldAccountPkCostCenterId.getString()) },
                    SLibConstants.EXEC_MODE_VERBOSE) > 0) {
                validation.setMessage("Ya existe un registro para el valor especificado en el campo '" + jlPkCostCenterId.getText() + "'.");
                validation.setComponent(jftPkCostCenterId);
            }
        }
        
        if (!validation.getIsError()) {
            if (moFieldDateEnd_n.getDate() != null && moFieldDateEnd_n.getDate().before(moFieldDateStart.getDate())) {
                validation.setMessage("La fecha del campo '" + jlDateEnd_n.getText() + "' no puede ser anterior a la fecha del campo '" + jlDateStart.getText() + "'.");
                validation.setComponent(jftDateEnd_n);
            }
        }

        return validation;
    }

