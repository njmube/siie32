    private void initComponentsExtra() {
        moSetting = null;

        moDialogFilterUnitType = new SDialogFilterUnitType(miClient);
        mnUnitTotalType = SDataConstantsSys.TRNX_TP_UNIT_TOT_QTY;
        moSetting = new STableSetting(SFilterConstants.SETTING_FILTER_UNIT_TP,  new int[] { mnUnitTotalType } );
        moSetting.setSetting(mnUnitTotalType);
        moTab.addSetting(moSetting);

        renderText();
    }

    private void actionUnitType() {
        moDialogFilterUnitType.formReset();
        moDialogFilterUnitType.formRefreshCatalogues();
        moDialogFilterUnitType.setValue(SDataConstantsSys.VALUE_TP_UNIT, mnUnitTotalType);
        moDialogFilterUnitType.setFormVisible(true);

        if (moDialogFilterUnitType.getFormResult() == erp.lib.SLibConstants.FORM_RESULT_OK) {
            mnUnitTotalType = moDialogFilterUnitType.getUnitType();
            moSetting.setSetting(mnUnitTotalType);
            moTab.updateSetting(moSetting);
        }

        renderText();
    }

    private void renderText() {
        switch (mnUnitTotalType) {
            case SDataConstantsSys.TRNX_TP_UNIT_TOT_QTY:
                jtfUnitType.setText("CANTIDAD");
                break;
            case SDataConstantsSys.TRNX_TP_UNIT_TOT_LEN:
                jtfUnitType.setText("LONGITUD");
                break;
            case SDataConstantsSys.TRNX_TP_UNIT_TOT_SURF:
                jtfUnitType.setText("SUPERFICIE");
                break;
            case SDataConstantsSys.TRNX_TP_UNIT_TOT_VOL:
                jtfUnitType.setText("VOLUMEN");
                break;
            case SDataConstantsSys.TRNX_TP_UNIT_TOT_MASS:
                jtfUnitType.setText("MASA");
                break;
            default:
                miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_UTIL_UNKNOWN_OPTION);
        }
    }

