    private void initComponentsExtra() {
        mnCompanyBranchId = SLibConstants.UNDEFINED;
        moSetting = new STableSetting(SFilterConstants.SETTING_FILTER_COB, mnCompanyBranchId);
        moDialogFilterCompanyBranch = new SDialogFilterCompanyBranch(miClient);

        updateFilter(miClient.getSessionXXX().getCurrentCompanyBranchId(), false);
    }

    private void updateFilter(int id, boolean updateSetting) {
        mnCompanyBranchId = id;

        renderCompanyBranchText();

        moSetting.setSetting(mnCompanyBranchId);

        if (updateSetting) {
            moTab.updateSetting(moSetting);
        }
        else {
            moTab.addSetting(moSetting);
        }
    }

    private void actionCompanyBranch() {
        moDialogFilterCompanyBranch.formReset();
        moDialogFilterCompanyBranch.formRefreshCatalogues();
        moDialogFilterCompanyBranch.setCompanyBranchId(mnCompanyBranchId);
        moDialogFilterCompanyBranch.setVisible(true);

        if (moDialogFilterCompanyBranch.getFormResult() == erp.lib.SLibConstants.FORM_RESULT_OK) {
            updateFilter(moDialogFilterCompanyBranch.getCompanyBranchId(), true);
        }
    }

    private void renderCompanyBranchText() {
        String text = "";

        if (mnCompanyBranchId == SLibConstants.UNDEFINED) {
            text = SLibConstants.TXT_ALL;
        }
        else {
            text = ((SDataBizPartnerBranch) SDataUtilities.readRegistry(
                    miClient, SDataConstants.BPSU_BPB, new int[] { mnCompanyBranchId }, SLibConstants.EXEC_MODE_SILENT)).getCode();
        }

        jtfCompanyBranch.setText(text);
        jtfCompanyBranch.setCaretPosition(0);
    }

