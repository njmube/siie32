    private void initComponentsExtra() {
        moYearPicker = new SGuiYearPicker(miClient);
        moSetting = new STableSetting(STableConstants.SETTING_FILTER_YEAR, (Object) miClient.getSessionXXX().getWorkingYear());
        miTableTab.addSetting(moSetting);
        renderYear();
    }

    private void renderYear() {
        jtfYear.setText("" + moSetting.getSetting());
    }

    private void actionYear() {
        moYearPicker.formReset();
        moYearPicker.setValue(SLibConstants.VALUE_YEAR, SLibUtilities.parseInt(jtfYear.getText()));
        moYearPicker.setFormVisible(true);

        if (moYearPicker.getFormResult() == SLibConstants.FORM_RESULT_OK) {
            moSetting.setSetting(moYearPicker.getValue(SLibConstants.VALUE_YEAR));
            miTableTab.updateSetting(moSetting);
            renderYear();
        }
    }

