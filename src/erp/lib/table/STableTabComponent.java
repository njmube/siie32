    private void actionClose() {
        int i = mjTabbedPane.indexOfTabComponent(this);
        if (i != -1) {
            if (mjTabbedPane.getComponentAt(i) instanceof STableTab) {
                ((STableTab) mjTabbedPane.getComponentAt(i)).tableTabClosed();
            }
            mjTabbedPane.removeTabAt(i);
        }
    }

    private void updateButtonCloseStatus() {
        int i = mjTabbedPane.indexOfTabComponent(this);
        if (i != -1) {
            jbClose.setIcon(i == mjTabbedPane.getSelectedIndex() ? moIconActive : moIconInactive);
        }
    }

