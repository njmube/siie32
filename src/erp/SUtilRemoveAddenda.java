    private void initComponentsExtra() {
        jtfXml.setText("");
        moFileChooser = new JFileChooser();
        moFileFilter = new FileNameExtensionFilter("XML archivo", "xml");
        moFileChooser.setAcceptAllFileFilterUsed(false);
        moFileChooser.setFileFilter(moFileFilter);
    }

    private void actionOpen() throws IOException {
        String sXml = "";
        BufferedReader bufferedReader = null;
        msXml = "";

        try {
            moFileChooser.showOpenDialog(moFileChooser);

            moFile = new File(moFileChooser.getSelectedFile().getAbsolutePath());
            bufferedReader = new BufferedReader(new InputStreamReader(new FileInputStream(moFile), "UTF8"));

            jtfXml.setText(moFile.getAbsolutePath());
            jtfXml.setCaretPosition(0);

            while ((sXml = bufferedReader.readLine()) != null) {
                msXml = msXml.concat(sXml);
            }
            
            msXml = SCfdUtils.removeNode(msXml, "cfdi:Addenda");
            msXml = msXml.replaceAll("www.sat.com", "www.sat.gob");
        }
        catch (FileNotFoundException e) {
            System.err.println(e);
        }
    }

    private void actionProcess() {
        String xmlName = "";
        BufferedWriter bw = null;

        if (jtfXml.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un comprobante.", "Advertencia!", JOptionPane.WARNING_MESSAGE);
            jbOpen.requestFocus();
        }
        else {
            try {
                xmlName = moFile.getAbsolutePath().substring(0, moFile.getAbsolutePath().indexOf(".")) + "_1.xml";

                bw = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(xmlName),"UTF8"));
                bw.write(msXml);
                bw.close();

                JOptionPane.showMessageDialog(this, SLibConstants.MSG_INF_FILE_CREATE + xmlName, "Archivo guardado", JOptionPane.INFORMATION_MESSAGE);
            }
            catch (IOException | HeadlessException e) {
                System.err.println(e);
                JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void actionClose() {
        dispose();
    }

