    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyCompanyBranch, SModConsts.BPSU_BPB, ((SSessionCustom) miClient.getSession().getSessionCustom()).getCurrentCompanyKey()[0], null);
        miClient.getSession().populateCatalogue(moKeySpot, SModConsts.LOGU_SPOT, SLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbSpotCompanyBranch) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyCompanyBranch.setValue(new int[] { moRegistry.getPkCompanyBranchId() });
        moKeySpot.setValue(new int[] { moRegistry.getFkSpotId() });

        setFormEditable(true);

        if (!moRegistry.isRegistryNew()) {
            moKeyCompanyBranch.setEnabled(false);
        }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbSpotCompanyBranch registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        moRegistry.setPkCompanyBranchId(moKeyCompanyBranch.getValue()[0]);
        moRegistry.setFkSpotId(moKeySpot.getValue()[0]);

        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();

        return validation;
    }
