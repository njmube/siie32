    @Override
    public void reloadCatalogues() {
        try {
            Vector<SGridRow> vRows = new Vector<>();

            moRegistry.getRows(miClient.getSession(), mnParamPkCurrencyId);
            for (SRowExchangeRate row : moRegistry.getRowsExchangeRates()) {

                vRows.add(row);
            }

            moGridEntries.populateGrid(vRows);
            moGridEntries.clearSortKeys();
            moGridEntries.setSelectedGridRow(0);

        } catch (SQLException e) {
            SLibUtils.showException(this, e);
        }
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SRowExchangeRate) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
        }

        if (moRegistry.isRegistryNew()) { }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SRowExchangeRate registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        return registry;
    }

    @Override
    public void setValue(int type, Object value) {
        switch (type) {
            case SModConsts.FIN_EXC_RATE:
                mnParamPkCurrencyId = (Integer) value;
                break;

            default:
        }
    }

    @Override
    public Object getValue(final int type) {
        Object value = null;

        switch (type) {
            case SModConsts.FIN_EXC_RATE:
                value = mdExchangeRate;
                break;
        }

        return value;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();

        return validation;
    }
