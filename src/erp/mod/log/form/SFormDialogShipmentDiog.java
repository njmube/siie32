    @Override
    public void reloadCatalogues() {
        try {
            Vector<SGridRow> vRows = new Vector<>();

            moRegistry.getRows(miClient.getSession(), mnParamFkShipmentCobId, mnParamFkWarehouseId);
            for (SRowShipmentDiogSupply row : moRegistry.getRowShipmentDiogSupply()) {

                vRows.add(row);
            }

            moGridEntries.populateGrid(vRows);
            moGridEntries.clearSortKeys();
            moGridEntries.setSelectedGridRow(0);

        } catch (SQLException e) {
            SLibUtils.showException(this, e);
        }
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SRowShipmentDiogSupply) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();

        }
        else {

        }

        if (moRegistry.isRegistryNew()) { }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SRowShipmentDiogSupply registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        return registry;
    }

    @Override
    public void setValue(int type, Object value) {
        switch (type) {
            case SModConsts.BPSU_BPB:
                mnParamFkShipmentCobId = (Integer) value;
                break;

            case SModConsts.CFGU_COB_ENT:
                mnParamFkWarehouseId = (int[]) value;
                break;

            default:
        }
    }

    @Override
    public Object getValue(final int type) {
        Object value = null;

        switch (type) {
            case SModConsts.TRN_DIOG:
                value = mnPrimarykeySelectedDiog;
                break;
        }

        return value;
    }

