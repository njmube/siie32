    @Override
    public void reloadCatalogues() {
        moFieldKeyGroup.initGroup();
        moFieldKeyGroup.addFieldKey(moKeyTaxBasic, SModConsts.FINU_TAX_BAS, SLibConsts.UNDEFINED, null);
        moFieldKeyGroup.addFieldKey(moKeyTax, SModConsts.FINU_TAX, SLibConsts.UNDEFINED, null);
        moFieldKeyGroup.populateCatalogues();
        moFieldKeyGroup.resetGroup();
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbAbpTax) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        moTextName.setValue(moRegistry.getName());
        moKeyTaxBasic.setValue(new int[] {  moRegistry.getFkTaxBasicId() });
        moKeyTax.setValue(new int[] { moRegistry.getFkTaxBasicId(), moRegistry.getFkTaxId() });

        populateAbpSettings();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        setFormEditable(true);
        addAllListeners();
    }

    @Override
    public SDbAbpTax getRegistry() throws Exception {
        SDbAbpTax registry = moRegistry.clone();
        HashMap<Integer, SAbpRegistry> rowsMap = new HashMap<Integer, SAbpRegistry>();

        if (registry.isRegistryNew()) {}

        registry.setName(moTextName.getValue());
        registry.setFkTaxBasicId(moKeyTaxBasic.getValue()[0]);
        registry.setFkTaxId(moKeyTax.getValue()[1]);

        for (SGridRow row : moGridAbpSettings.getModel().getGridRows()) {
            rowsMap.put(((SAbpRegistry) row).getAccountTypeId(), (SAbpRegistry) row);
        }

        registry.setAbpRows(rowsMap);

        return registry;
    }

