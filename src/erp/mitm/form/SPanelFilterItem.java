    private void initComponentsExtra() {
        moItem = null;

        miPicker = miClient.getOptionPicker(SDataConstants.ITMX_ITEM_IOG);
        miPicker.formReset();
        miPicker.setFilterKey(null);
        miPicker.formRefreshOptionPane();

        renderItem();
    }

    private void renderItem() {
        if (moItem == null) {
            jtfItem.setText(mbIsNoneEnabled ? SLibConstants.TXT_NONE : SLibConstants.TXT_ALL);
        }
        else {
            jtfItem.setText(moItem.getItem());
        }

        jtfItem.setCaretPosition(0);
        jtfItem.setToolTipText(TXT_TIP + ": " + jtfItem.getText());
    }

    private void actionItem() {
        miPicker.formReset();
        miPicker.formRefreshOptionPane();
        miPicker.setFormVisible(true);

        if (miPicker.getFormResult() != SLibConstants.FORM_RESULT_OK) {
            moItem = null;
            moTableTab.updateSetting(new STableSetting(STableConstants.SETTING_FILTER_ITM_ITEM, new int[] { SLibConstants.UNDEFINED }));
        }
        else {
            moItem = (SDataItem) SDataUtilities.readRegistry(miClient, SDataConstants.ITMU_ITEM, miPicker.getSelectedPrimaryKey(), SLibConstants.EXEC_MODE_VERBOSE);
            moTableTab.updateSetting(new STableSetting(STableConstants.SETTING_FILTER_ITM_ITEM, moItem.getPrimaryKey()));
        }

        renderItem();
    }

